#include <bits/stdc++.h>
using namespace std;
const int MAXK = 752;
int matrix[MAXK][MAXK];

int main()
{
    int k;
    while (scanf("%d", &k) == 1)
    {
        for (int i = 0; i < k; i++)
        {
            for (int j = 0; j < k; j++)
            {
                scanf("%d", &matrix[i][j]);
            }
        }

        

    }
    return 0;
}
